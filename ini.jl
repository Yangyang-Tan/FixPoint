function iniO1du1(A0, rho0)

    [
        A0 * rho0^2 - (1 / (75 * A0 * pi^2 * rho0^3)),
        2 * A0 * rho0 + 1 / (25 * A0 * pi^2 * rho0^4),
        2 * A0 - (4 / (25 * A0 * pi^2 * rho0^5)),
    ]
end


function iniO1du0(A0, rho0)
    [
        1 / (150 * A0 * pi^2 * rho0^2) + (A0 * rho0^3) / 3,
        A0 * rho0^2 - (1 / (75 * A0 * pi^2 * rho0^3)),
        2 * A0 * rho0 + 1 / (25 * A0 * pi^2 * rho0^4),
    ]
end


function inidu0(γ::T, rho0::T; d::T=T(3), n::T=T(1), η::T=T(0)) where {T}
    c1 = (2 + d - η) / (pi^(d / 2) * (d * (2 + d) * gamma(d / 2) * 2^(d - 1)))
    if 3 + 1e-6 > d > 3 - 1e-6
        return [
            γ *
            rho0^3 *
            (
                1 + rho0^(-5) * c1 * (5 * n - 4) / (75 * γ^2) -
                rho0^(-7) * c1 * (25 * n - 24) / (1575 * γ^3) +
                rho0^(-9) * (125n - 124) * c1 / (30375 * γ^4)
            ),
            (c1 * (248 - 250 * n)) / (10125 * rho0^7 * γ^3) +
            (4 * c1 * (-24 + 25 * n)) / (1575 * rho0^5 * γ^2) +
            (2 * c1 * (4 - 5 * n)) / (75 * rho0^3 * γ) +
            3 * rho0^2 * γ,
            (14 * c1 * (-124 + 125 * n)) / (10125 * rho0^8 * γ^3) +
            (4 * c1 * (24 - 25 * n)) / (315 * rho0^6 * γ^2) +
            (2 * c1 * (-4 + 5 * n)) / (25 * rho0^4 * γ) +
            6 * rho0 * γ,
        ]
    elseif 2.5 + 1e-6 > d > 2.5 - 1e-6
        return [
            (2 * c1 * (-728 + 729 * n)) / (1549125 * rho0^12 * γ^3) +
            (2 * c1 * (80 - 81 * n)) / (26325 * rho0^8 * γ^2) +
            (2 * c1 * (-8 + 9 * n)) / (405 * rho0^4 * γ) +
            rho0^5 * γ,
            (8 * c1 * (728 - 729 * n)) / (516375 * rho0^13 * γ^3) +
            (16 * c1 * (-80 + 81 * n)) / (26325 * rho0^9 * γ^2) +
            (8 * c1 * (8 - 9 * n)) / (405 * rho0^5 * γ) +
            5 * rho0^4 * γ,
            (104 * c1 * (-728 + 729 * n)) / (516375 * rho0^14 * γ^3) +
            (16 * c1 * (80 - 81 * n)) / (2925 * rho0^10 * γ^2) +
            (8 * c1 * (-8 + 9 * n)) / (81 * rho0^6 * γ) +
            2,
        ]
    else
        return [
            γ * rho0^(d / (d - 2 + η)),
            (d * rho0^(-1 + d / (-2 + d + η)) * γ) / (-2 + d + η),
            -((d * rho0^(-2 + d / (-2 + d + η)) * γ * (-2 + η)) / (-2 + d + η)^2),
        ]
    end
end

